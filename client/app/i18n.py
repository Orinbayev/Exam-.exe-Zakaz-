    """Joriy til uchun tarjima qaytaradi."""
    lang = Config.get("lang", "uz")
    val = _T.get(lang, _T["uz"]).get(key) or _T["uz"].get(key, key)
    if kwargs:
        try:
            return val.format(**kwargs)
        except (KeyError, IndexError):
            return val
    return val


